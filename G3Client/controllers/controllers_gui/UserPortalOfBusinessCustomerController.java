public class UserPortalOfBusinessCustomerController extends AbstractBiteMeController implements Initializable{
	
	/**
	 * Class members description:
	 */
	
	/**
	 * The FXMLLoader of the current screen.
	 */
	public static FXMLLoader loader;
	
	/**
	 * A static object of the current class.
	 */
	private static UserPortalOfBusinessCustomerController userPortalOfBusinessCustomerController;
	
	@FXML
	/**
	 * Exit Button.
	 */
	private Button btnExit;
	
	@FXML
	/**
	 * Log Out Button.
	 */
	private Button btnLogout;
	
	@FXML
	/**
	 * customer Name Label.
	 */
	private Label customerNameLabel;
	
	@FXML
	/**
	 * Help Button.
	 */
	private Button btnHelp;
	
	@FXML
	/**
	 * Start Order Button.
	 */
	private Button btnStartOrder;

    @FXML
    /**
	 * View Orders Button.
	 */
    private Button viewOrdersBtn;
    
	@FXML
	/**
	 * Company Name Label.
	 */
	private Label companyNameLabel;
	
    @FXML
    /**
     * The text to display the company name.
     */
    private Text companyName;
    
    @FXML
    /**
     * The text to display the customer name.
     */
    private Text businessCustomerName;
    
    @FXML
    /**
     * the text to display the customer status.
     */
    private Text statusText;
    
    /**
     * This method will disconnect the specific client,
     * log out the connected user and exit.
     * 
     * @param event ActionEvent of javaFX.
     */
	@FXML
	public void getExitBtn(ActionEvent event) {
		// TODO Autogenerated
		Message message= new Message(Task.LOGOUT,Answer.WAIT_RESPONSE,connectedUser);
		sendToClient(message);
		connectedUser=null;
		Message disconnectMessage= new Message(Task.CLIENT_DICONNECT,Answer.WAIT_RESPONSE,null);
		sendToClient(disconnectMessage);
		System.exit(0);
	}
	
	 /**
     * This method will log out the connected user,
     * and load the login screen again.
     * 
     * @param event ActionEvent of javaFX.
     */
	@FXML
	public void getLogoutBtn(ActionEvent event) {
		Message message= new Message(Task.LOGOUT,Answer.WAIT_RESPONSE,connectedUser);
		sendToClient(message);
		connectedUser=null;
		setToLoginScreen(event);
	}
	
	/**
	 * This is pop message for the help button.
	 * 
	 * @param event ActionEvent of javaFX.
	 */
	@FXML
	public void getHelpBtn(ActionEvent event) {
		PopUpMessages.helpMessage("This is you'r the User-Portal, from here you can access the system functionalities!");	

	}
	
	/**
	 * When preset start order button,
	 * the screen will change to the first screen in the 
	 * order process which is the W4C identification.
	 * 
	 * @param event
	 */
	@FXML
	public void getStartOrder(ActionEvent event) {
		((Node) event.getSource()).getScene().getWindow().hide(); // hiding primary window
		OrderW4cIdentificationScreenController w4cIdentificationController = new OrderW4cIdentificationScreenController();
		w4cIdentificationController.initW4cIdentificationScreen(); // call the init of the next screen
	}
	
	/**
	 * This method will load the
	 * View Order screen.
	 * 
	 * @param event ActionEvent of javaFX.
	 */
    @FXML
    public void getViewOrdersBtn(ActionEvent event) {
     	((Node) event.getSource()).getScene().getWindow().hide(); // hiding primary window
    	WatchOrderHistoryScreenController watchOrderHistoryScreenController = new WatchOrderHistoryScreenController();
    	watchOrderHistoryScreenController.initOrderHistoryScreen(); // call the init of the next screen
    }
	
	/**
	 * Returns to login screen
	 * 
	 * @param event ActionEvent of javaFX.
	 */
	private void setToLoginScreen(ActionEvent event) {
		Platform.runLater(new Runnable() {
			@Override
			public void run() {
				FXMLLoader loader = new FXMLLoader();
				Pane root;
				try {
					Stage Stage = new Stage();
					Stage.setResizable(false);
					root = loader.load(getClass().getResource("/fxmls/LoginScreen.fxml").openStream());
					LoginScreenController LSC = loader.getController();
					LSC.initLoginScreen();
					Stage.setOnCloseRequest(new EventHandler<WindowEvent>() {
						@Override
						public void handle(WindowEvent event) { 
							event.consume();
							Stage.close();
						}
					});
				} catch (IOException e) {
					e.printStackTrace();
				}
				((Node) event.getSource()).getScene().getWindow().hide();
			}
		});
	}
	
	/**
	 * This is the initialization function for this 
	 * screen.
	 * 
	 * @param primaryStage The stage of the screen.
	 * @param fxmlPath The fxml path of the current screen.
	 */
	public void initBusinessCustomerUserPortal(Stage primaryStage,String fxmlPath) {
		Platform.runLater(new Runnable() {
			@Override
			public void run() {
				loader = new FXMLLoader();
				Pane root;
				try {
					primaryStage.hide(); 
					Stage Stage = new Stage();
					Stage.setResizable(false);
					root = loader.load(getClass().getResource(fxmlPath).openStream());
					userPortalOfBusinessCustomerController = loader.getController();
					Scene scene = new Scene(root);
					Stage.initStyle(StageStyle.UNDECORATED);
					scene.setOnMousePressed(pressEvent -> {
					    scene.setOnMouseDragged(dragEvent -> {
					    	Stage.setX(dragEvent.getScreenX() - pressEvent.getSceneX());
					    	Stage.setY(dragEvent.getScreenY() - pressEvent.getSceneY());
					    });
					});
					scene.getStylesheets().add(getClass().getResource("/css/G3_BiteMe_Main_Style_Sheet.css").toExternalForm());
					Stage.setTitle("Business Customer Main Screen");
					Stage.setScene(scene);
					Stage.show();
				} catch (IOException e) {
					e.printStackTrace();
				}
			}
		});
	}
	
	/**
	 * Init customer portal again.
	 */
	public void initPortalAgain() {
		loader = new FXMLLoader();
		Stage primaryStage = new Stage();
		Pane root = null;
		try {
			root = loader.load(getClass().getResource("/fxmls/UserPortalOfBusinessCustomer.fxml").openStream());
			userPortalOfBusinessCustomerController = loader.getController();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		Scene scene = new Scene(root);
		primaryStage.initStyle(StageStyle.UNDECORATED);
		scene.setOnMousePressed(pressEvent -> {
		    scene.setOnMouseDragged(dragEvent -> {
		    	primaryStage.setX(dragEvent.getScreenX() - pressEvent.getSceneX());
		    	primaryStage.setY(dragEvent.getScreenY() - pressEvent.getSceneY());
		    });
		});
		scene.getStylesheets().add(getClass().getResource("/css/G3_BiteMe_Main_Style_Sheet.css").toExternalForm());
		primaryStage.setScene(scene);
		primaryStage.show();/* show the new screen */
	}
	
	/**
	 * This method sets the correct values of the businessCustomer in portal.
	 * 
	 * @param arg0
	 * @param arg1
	 */
	@Override
	public void initialize(URL arg0, ResourceBundle arg1) {
		businessCustomerName.setText(connectedUser.getUserFirstName());
		companyName.setText(((BusinessCustomer)connectedUser).getcompanyOfBusinessCustomer().getCompanyName());
		statusText.setText(connectedUser.getStatusInSystem().toString());
		if(connectedUser.getStatusInSystem().equals(ConfirmationStatus.FROZEN)) {
			btnStartOrder.setDisable(true);
			viewOrdersBtn.setDisable(true);
		}
		
	}
}
